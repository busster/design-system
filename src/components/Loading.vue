<template>
  <div class="loading">
    <div class="loader" :style="loadingStyles">Loading...</div>
  </div>
</template>

<script>

export default {
  name: 'Loading',
  props: {
    size: {
      type: String,
      default: 'lg',
      validator: (val) => ['xtsm', 'sm', 'md', 'lg'].includes(val)
    },
    fullScreen: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    loadingStyles () {
      const fontSizeMap = {
        xtsm: '17px',
        sm: '30px',
        md: '60px',
        lg: '90px'
      }
      return {
        'font-size': fontSizeMap[this.size]
      }
    },
    loadingClasses () {
      let classes = ['loading']
      if (this.fullScreen) classes.push('full-screen')
      return this.classes
    }
  }
}
</script>

